class LanguageManager:
    """Base language manager usable by both TUI and GUI."""
    def __init__(self, initial="en"):
        self.current = initial

    def t(self, key: str) -> str:
        return STRINGS[self.current].get(key, key)

    def toggle(self):
        self.current = "hu" if self.current == "en" else "en"
